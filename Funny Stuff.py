hourspernight = input('How many hours do you sleep per night?')
hoursperweek = float(hourspernight) * 7
print('You sleep ',hoursperweek,' hours per week.')
hourspermonth = hoursperweek * 4.35
print('You sleep ',hourspermonth,' hours a month.')
hoursperyear = hourspermonth * 12
print('You sleep ',hoursperyear,' hours a year.')
